-- --------------------------------  SCHEMAS -------------------------------- --

CREATE SCHEMA reviewer AUTHORIZATION sa;


-- --------------------------------  TABLES --------------------------------- --

CREATE CACHED TABLE reviewer.app_properties(	
	version VARCHAR(100)
);
INSERT INTO reviewer.app_properties(version) VALUES('0.2.0');


CREATE CACHED TABLE reviewer.literature_reviews(
	literature_review_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	title VARCHAR(5000),
);

CREATE CACHED TABLE reviewer.searches(
	search_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	search_type VARCHAR(3),
	query_string VARCHAR(50000),
	literature_review_id INTEGER,
	CONSTRAINT s_literature_review_fk FOREIGN KEY(literature_review_id) REFERENCES reviewer.literature_reviews(literature_review_id) ON DELETE CASCADE,
);


CREATE CACHED TABLE reviewer.query_info(
	query_info_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	total_found INTEGER,
	total_fetched INTEGER,
	source VARCHAR(50000),
	search_id INTEGER,
	CONSTRAINT qi_search_fk FOREIGN KEY(search_id) REFERENCES reviewer.searches(search_id),
);

CREATE CACHED TABLE reviewer.critireon(
	criteria_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY, 
	name VARCHAR(50000),
	literature_review_id INTEGER,
	CONSTRAINT c_literature_review_fk FOREIGN KEY(literature_review_id) REFERENCES reviewer.literature_reviews(literature_review_id) ON DELETE CASCADE,
);

CREATE CACHED TABLE reviewer.phases(
	phase_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	literature_review_id INTEGER,
	CONSTRAINT p_literature_review_fk FOREIGN KEY(literature_review_id) REFERENCES reviewer.literature_reviews(literature_review_id) ON DELETE CASCADE,
);


-- is 'group' a reserved word? I've put groups because eclipse indicated that 'group' is a reserved word --
CREATE CACHED TABLE reviewer.groups(
	group_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	phase_id INTEGER,
	CONSTRAINT g_phase_fk FOREIGN KEY(phase_id) REFERENCES reviewer.phases(phase_id) ON DELETE CASCADE,
);


CREATE CACHED TABLE reviewer.study_analisys(
	study_analisys_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	phase_id INTEGER,
	CONSTRAINT sa_phase_fk FOREIGN KEY(phase_id) REFERENCES reviewer.phases(phase_id) ON DELETE CASCADE,	
);

CREATE CACHED TABLE reviewer.evaluator(
	evaluator_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY, 
	name VARCHAR(50000),
	group_id INTEGER,
	CONSTRAINT e_groups_fk FOREIGN KEY(group_id) REFERENCES reviewer.groups(group_id) ON DELETE CASCADE,
);

CREATE CACHED TABLE reviewer.evaluator_analysis(
	evaluation_analysis_id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	status INTEGER,
	study_analisys_id INTEGER,
	evaluator_id INTEGER,
	criteria_id INTEGER,
	CONSTRAINT ea_study_analisys_fk FOREIGN KEY(study_analisys_id) REFERENCES reviewer.study_analisys(study_analisys_id) ON DELETE CASCADE,
	CONSTRAINT ea_evaluator_fk FOREIGN KEY(evaluator_id) REFERENCES reviewer.evaluator(evaluator_id) ON DELETE CASCADE,
	CONSTRAINT ea_criteria_fk FOREIGN KEY(criteria_id) REFERENCES reviewer.critireon(criteria_id) ON DELETE CASCADE,
);



